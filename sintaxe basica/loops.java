

public class loops {

    public static void main(String[] args) {
        // Loop for
        for (int i = 0; i < 5; i++) {
            System.out.println("Contagem: " + i);
        }

        // Loop while
        int j = 0;
        while (j < 5) {
            System.out.println("Contagem: " + j);
            j++;
        }

        // Loop do-while
        int k = 0;
        do {
            System.out.println("Contagem: " + k);
            k++;
        } while (k < 5);
    }
    
}
