

public class variaveis {

    public static void main(String[] args) {
        // Variáveis
        int idade = 20; // idade é uma variável do tipo inteiro
        String nome = "João"; // nome é uma variável do tipo String (texto)
        double altura = 1.75; // altura é uma variável do tipo double (número decimal)
        boolean estudante = true; // estudante é uma variável do tipo boolean (verdadeiro ou falso)

        // Exibindo as variáveis
        System.out.println("Nome: " + nome);
        System.out.println("Idade: " + idade);
        System.out.println("Altura: " + altura);
        System.out.println("Estudante: " + estudante);
    
} }


